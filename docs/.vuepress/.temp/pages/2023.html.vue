<p>可是，搞清楚自己想要什么、接下来各阶段最值得先重点突破什么，也不算容易。难点主要有三：</p>
<p><strong>首先，这很难用三两周，就得出心里踏实的答案，需要经过一段时间探索尝试、交叉验证并迭代</strong>。短则两三个月，长则半年。</p>
<p>如果没使用专业的工具方法，甚至数十年也难有心里踏实的答案。</p>
<p>因为很多人探索解法答案的路径，偏本能尝试——</p>
<p>像下图左边一样，随机冒出一个可能的解法，然后开始尝试。一个不成功的想法，被另一个不成功的想法代替，然后继续做下去，直到找到成功的答案。</p>
<p>结果随机性很大，试错成本高，还易陷入自我怀疑、进入恶性循环。对人生角色越来越多、责任越来越重、时间宝贵的我们来说，更易如此。</p>
<p><img src="https://cdn.sunnyhuang.net/share/QTfheG.png" alt="QTfheG"></p>
<br>
<p><strong>其次，外界在快速变化，我们自己也在变，之前合适的很可能不再适用</strong>。</p>
<p>就像航海家出海远航，拿着出发前测绘的前方十海里航海地图，走了一段，更了解这片海域，也更了解航海所需条件、自己的航船情况，甚至随着体验变多，向往之处也逐步变化。</p>
<p>如果手里的航海地图，还是之前的版本，没有及时向前侦查测绘、更新完善，自然难以决策靠谱航线、避开风险，多走好些弯路，才到真正向往之地；或者容易随波逐流，甚至途中翻船。</p>
<p><strong>其实很多时候，我们就是那样的航海家</strong>——比如只是在年初花三两天制定目标，或者只考虑今年的目标规划，或者一年才更新一次人生目标规划。</p>
<p><strong>而人生就像大海一样，变化莫测，岔路无数。</strong></p>
<p><strong>如果不持续随着进展，更新迭代对人生关键问题的思考、接下来的目标规划，自然难以根据最新情况做出靠谱决策、心怀希望继续远航，变得缺乏生机和动力。</strong></p>
<br>
<p><strong>此外，决策不是结束，而是开始，之后还有漫漫长路。如果不能跨过一个又一个挑战持续前进，依然难以到达向往之地。</strong></p>
<p><strong>而且，整个过程如果独自进行，常会磨磨蹭蹭，甚至不了了之</strong>。</p>
<p>别问我怎么知道的——哪怕是常被称赞执行力很强的人如我，也未能幸免……</p>
