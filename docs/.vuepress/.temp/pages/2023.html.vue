<blockquote>
<p>这种「懒」，不是躺平、打游戏、刷剧，而是<strong>不深度思考、不拆解分析、不转化视角，不愿意按照套路去「思考、拆解、计划、实施、观测、反思」，去拆解分析一个问题变成10小步，然后按部就班走10步</strong>。</p>
</blockquote>
<p><strong>这种「懒」，也正是很多人「有行动却低效能」的主要原因</strong>，会令人搞东搞西每天也没闲着，但回头看时，却很少发现自己满意的成果和突破；或常在救火，难以从容舒展。</p>
<br>
<p><strong>那怎么办？人生还有几十年，决策岔路口无数，怎么有更清晰靠谱的自我认知，走出更适合自己的人生之路，并跨过重重挑战，踏实前进？</strong></p>
<p>解决办法一般分两类：</p>
