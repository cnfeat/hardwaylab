<p>爱与成就，是人类人生故事的两大主题，也是我们人生难题的主要范畴。比如：</p>
<blockquote>
<ul>
<li>我该不该换工作/转行/创业？去做什么呢？</li>
<li>去不去读个MBA呢？</li>
<li>这段关系该不该继续？是在一起还是分手？该不该结婚/离婚？</li>
<li>接下来该不该换个轻松的工作，有更多时间关照自己/投入一段关系/陪伴家人？还是换个更有挑战的项目，加速提升个人能力/实现自我价值？或是做个副业，多加点收入？该选哪个项目呢？该投入多少资源呢？</li>
<li>生不生娃？对我来说，啥时候适合生第一个/下一个？谁来带？怎么安排？</li>
<li>传承什么给孩子？给孩子重点传承哪些特质呢？怎么传承更合适？</li>
<li>买学区房吗？孩子的升学路线怎么设计，国内还是海外，公立还是私立？</li>
<li>爸妈养老怎么安排？怎样让他们老有所依、安康充实？怎么推进更合适？</li>
<li>……</li>
</ul>
</blockquote>
<p>面对这些决策岔路，很多人纠结久久，依然徘徊。为什么？不是缺方法，也不是缺优质讯息，而是困于：不知道自己想要什么。</p>
<p><strong>毕竟人生岔路口，没有标准答案。</strong></p>
<p><strong>要想做出适合自己的决策，前提是有清晰靠谱的「自我认知」——</strong></p>
<p><strong>知道自己想成为 什么样的人、想过怎样的生活、看重什么、可以放弃什么；<br>
现状相较心中期待，主要差距在哪里；<br>
想缩小这些差距，目前有哪些优劣势、资源机会、风险和预案；<br>
为了实现心中理想生活，接下来不同阶段最值得重点突破什么，怎样算是搞定了等等。</strong></p>
<p>如此，才好结合外部讯息、最新进展，像下图一样，找出「想做-可做-能做」三者交集，得出靠谱决策，明确接下来最值得做什么。
