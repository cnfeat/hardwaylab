<p>产品货架是你对创造的热爱，身份组合是你对价值的思考，生产方式是你对空间的设计，可能自我是你对自我的期盼，三者组合能促使你主动思考、学习、成长、持续改善，通过达成无数小进步，探索到更好的人生问题解决办法，让你宏观有耐心，微观有效率，成为可能自我。</p>
<p>这套系统关注以人为本的效率提升，关注人生信念的持续实现，贯穿你的整个人生，实现并维护以下两种人生状态：</p>
<p><strong>1）当下身心富足，未来健康从容。</strong></p>
<p>生产力是生活幸福感、创造心流、优秀工作习惯与趁手工具的结合，从而抵达身心富足的一种状态。</p>
<p>它包括且不限于以下关键词：良好睡眠、饮食运动、坚定拒绝、专注简洁等。</p>
<p><strong>2）专注价值创造，成为可能自我。</strong></p>
<p>生产力是勇于面对不确定，建立自我安全感，达成自我契约，专注价值创造，成为可能自我的过程。</p>
<p>它包括且不限于以下关键词：终身学习、超越技能，自主优化、工作分发、自动化操作、效率工具等。</p>
